# Membuat Variabel
# String Variabel
namaMhs = "Hello World"
print(namaMhs)
print(type(namaMhs))
# Variabel Integer
umurMhs = 20
print(umurMhs)
print(type(umurMhs))
# Variabel Float
ipkMhs = 3.5
print(ipkMhs)
print(type(ipkMhs))
# Variabel Bolean
lulusMhs = 3.5>3.0
print(lulusMhs)
print(type(lulusMhs))

